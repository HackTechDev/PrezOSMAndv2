                    <h3>OpenStreetMap</h3>
                    <p>
* Un service collaboratif de cartographie en-ligne.</br>
* OSM est un projet de cartographie qui a pour but de constituer une base de données géographiques libre du monde.<br/>
* Il a été mis en route en juillet 2004 par Steve Coast au University College de Londres.<br/>
* Il permet la collaboration de tout utilisateur volontaire par l'utilisation de moyens informatiques basés sur Internet.<br/>
                    </p>
                

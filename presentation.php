                    <h3>OsmAnd</h3>
                    <p>
* OsmAnd (OSM Automated Navigation Directions) </br>
* Une application cartographique et de navigation avec accès aux données libres OpenStreetMap (OSM).<br/>
* Toutes les données cartographiques peuvent être stockées sur la carte mémoire de votre appareil.<br/>
* OsmAnd fournit un routage GPS, avec guidage visuel et vocal, pour la voiture, le vélo ou le piéton.<br/>
* Toutes les fonctionnalités principales fonctionnent à la fois en mode en ligne et hors ligne.<br/>
